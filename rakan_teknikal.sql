-- USEFUL VIEWS
-- Pending applications with student + job details (used by admin dashboard)
CREATE OR REPLACE VIEW v_pending_applications AS
  SELECT
    a.application_id,
    u.name        AS student_name,
    s.matric_no,
    j.job_title,
    j.location,
    a.apply_date,
    a.status
  FROM application a
  JOIN user    u ON a.user_id = u.user_id
  JOIN student s ON a.user_id = s.user_id
  JOIN job     j ON a.job_id  = j.job_id
  WHERE a.status = 'pending'
  ORDER BY a.apply_date ASC;

-- Dashboard stats summary (used by admin dashboard counters)
CREATE OR REPLACE VIEW v_dashboard_stats AS
  SELECT
    (SELECT COUNT(*) FROM job   WHERE status = 'open')                          AS open_jobs,
    (SELECT COUNT(*) FROM application)                                          AS total_applications,
    (SELECT COUNT(*) FROM application WHERE status = 'pending')                 AS pending_applications,
    (SELECT COUNT(*) FROM application WHERE status = 'approved')                AS approved_applications,
    (SELECT COUNT(*) FROM user WHERE role = 'student' AND status = 'active')    AS registered_students;
